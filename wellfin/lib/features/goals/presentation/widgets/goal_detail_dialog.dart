                      const SnackBar(content: Text('„Éû„Ç§„É´„Çπ„Éà„Éº„É≥„ÇíËøΩÂä†„Åó„Åæ„Åó„Åü')),
                    );
                  }
                },
                child: const Text('ËøΩÂä†'),
              ),
            ],
          );
        },
      ),
    );
  }
